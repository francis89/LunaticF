package spring.model.memo_l;

public class Memo_lDTO {
	private int memono;
	private String mtitle;
	private String mcontent;
	private String mdate;
	private int mviewcnt;
	private String mpasswd;
	private String mnick;

	public int getMemono() {
		return memono;
	}

	public void setMemono(int memono) {
		this.memono = memono;
	}

	public String getMtitle() {
		return mtitle;
	}

	public void setMtitle(String mtitle) {
		this.mtitle = mtitle;
	}

	public String getMcontent() {
		return mcontent;
	}

	public void setMcontent(String mcontent) {
		this.mcontent = mcontent;
	}

	public String getMdate() {
		return mdate;
	}

	public void setMdate(String mdate) {
		this.mdate = mdate;
	}

	public int getMviewcnt() {
		return mviewcnt;
	}

	public void setMviewcnt(int mviewcnt) {
		this.mviewcnt = mviewcnt;
	}

	public String getMpasswd() {
		return mpasswd;
	}

	public void setMpasswd(String mpasswd) {
		this.mpasswd = mpasswd;
	}

	public String getMnick() {
		return mnick;
	}

	public void setMnick(String mnick) {
		this.mnick = mnick;
	}

}
